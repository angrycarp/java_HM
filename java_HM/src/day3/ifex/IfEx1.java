package day3.ifex;

import java.util.Scanner;

public class IfEx1 {

	public static void main(String[] args) {
		// 정수 num가 0이면 0입니다라고 출력하는 코드를 입력하세요
		// 정수 num가 0이다
		// 정수 num가 0과 같다
		int num = 10;
		if(num == 0) {
			// 0입니다라고 출력;
			System.out.println("0 은 0입니다.");
		}
		// 정수 num가 0이 아니면 num은 0이 아닙니다라고 출력하는 코드를 작성하세요
		// num가 0이 아니다
		// num가 0과 같지 않다
		if(num != 0) {
			System.out.println(num + "은 0이 아닙니다.");
		}

	}
	

}
